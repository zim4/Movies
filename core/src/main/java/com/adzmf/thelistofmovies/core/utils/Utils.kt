    fun dateFormatter(context: Context, input: String): String {
        val date = input.split("-")
        return if (date.size == 3) {
            val months = context.resources.getStringArray(R.array.short_month)
            val d = date[2].toInt()
            val m = date[1].toInt()
            val month = months[m - 1]
            val y = date[0].toInt()
            "$month $d, $y"
        } else {
            "-"
        }
    }

