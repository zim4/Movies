    fun listToComma(list: List<String>): String {
        var result = ""
        for (i in list.indices) {
            result += list[i]
            if (i != list.size - 1) result += ", "
        }
        return result
    }

    fun minuteToHM(minute: Int): String {
        val m = minute % 60
        val h = (minute - m) / 60
        val sM = if (m != 0) "${m}m" else ""
        val sH = if (h != 0) "${h}h " else ""
        return "$sH$sM"
    }

